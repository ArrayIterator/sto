    /**
     * @param string $code
     * @return mixed|null
     */
    public function getContinentByCountryCode(string $code)
    {
        $result = $this->getCountryByCode($code);
        return $result ? $result['continent'] : null;
    }

    /**
     * @param string $code
     * @return mixed|null
     */
    public function getTimeZonesByCountryCode(string $code)
    {
        $result = $this->getCountryByCode($code);
        return $result ? $result['timezones'] : null;
    }

    /**
     * @param string $code
     * @return mixed|null
     */
    public function getCurrenciesByCountryCode(string $code)
    {
        $result = $this->getCountryByCode($code);
        return $result ? $result['currencies'] : null;
    }

    /**
     * @param string $code
     * @return mixed|null
