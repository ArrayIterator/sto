    public function getNumberByCountryCode(string $code)
    {
        $result = $this->getCountryByCode($code);
        return $result ? $result['numeric'] : null;
    }

    /**
     * @param string $code
     * @return array|null
     */
    public function getCountryByCode(string $code)
    {
        static $_3_letter_code = [];
        $code = strtoupper($code);
        if (strlen($code) > 3) {
            return null;
        }
        if (strlen($code) === 3) {
            $realCode = $_3_letter_code[$code] ?? false;
            if ($realCode === null) {
                foreach ($this->countries as $c => $country) {
                    if ($country['code']['alpha3'] === $code) {
                        $realCode = $c;
                        break;
                    }
                }
                $_3_letter_code = $realCode;
            }
            $code = $realCode;
            if (!$code) {
                return null;
            }
        }
        return $this->countries[$code] ?? null;
    }
